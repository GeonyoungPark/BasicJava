package chapter06;

public class Printer {
	int x;
	boolean y;
	double z;
	String name;
	
	void println(int x) {
		System.out.println(x);
		
	}
	
	void println(boolean y) {
		System.out.println(y);
		
	}
	
	void println(double z) {
		System.out.println(z);
	}
	
	void println(String name) {
		System.out.println(name);
	}
	
}
